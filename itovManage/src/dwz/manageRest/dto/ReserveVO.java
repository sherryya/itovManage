package dwz.manageRest.dto;

public class ReserveVO {
	public final static int PAGE_SHOW_COUNT = 10;
	private int pageNum = 1;
	private int pageSize =0;
	private int totalCount = 0;
	private String name;
	private String phone_num="";
	private String reserve_code="";
	private String is_deal;
	
	public int getPageNum() {
		return pageNum;
	}
	public void setPageNum(int pageNum) {
		this.pageNum = pageNum;
	}
	public int getPageSize() {
		return pageSize > 0 ? pageSize : PAGE_SHOW_COUNT;
	}
	public void setPageSize(int pageSize) {
		this.pageSize = pageSize;
	}
	public int getTotalCount() {
		return totalCount;
	}
	public void setTotalCount(int totalCount) {
		this.totalCount = totalCount;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPhone_num() {
		return phone_num;
	}
	public void setPhone_num(String phone_num) {
		this.phone_num = phone_num;
	}
	public String getReserve_code() {
		return reserve_code;
	}
	public void setReserve_code(String reserve_code) {
		this.reserve_code = reserve_code;
	}
	public static int getPageShowCount() {
		return PAGE_SHOW_COUNT;
	}
	public String getIs_deal() {
		return is_deal;
	}
	public void setIs_deal(String is_deal) {
		this.is_deal = is_deal;
	}
}
